package exemplos;

public class Exemplo02 {
    public static void main(String[] args) {
        System.out.println(7 + 5);
        System.out.println("7 + 5");
        System.out.println("Soma = " + 7 + 5); // + realiza concatenação de texto
        System.out.println("Soma = " + (7 + 5)); // () altera a ordem de execução
    }
}
